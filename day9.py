year = int(input("Hey, what year were you born? "))
if year >= 1996 :
  print("Yoo, you are a Generation Z! \U0001F606")
elif year >= 1982 :
  print("Hey, Millenial! \U0001F60A")
elif year >= 1965 :
  print("You are a Generation X \U0001F60E!")
elif year >= 1947 :
  print("Hey Baby Boomer! \U0001F60C")
elif year >= 1925 :
  print("You are a Traditionalist \U0001F60D!")
else:
  print("Enter valid age!")